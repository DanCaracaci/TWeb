    <h2 onclick="slide(div2,s2)" style="position: relative; text-align: left">Marii maeștri<span id="s2">ᐱ</span></h2>
    <div class="block" id="div2">
    <p><img class="image right" src="../image/armoiriessvg.png" alt="Sigiliul Marelui Maestru Bertrand de Blanchefort (1109–1169)">
        Începând cu fondatorul Hugues de Payens, din 1118-1119 cel mai mare grad în cadrul Ordinului a fost cel de Mare Maestru, o poziţie câştigată pe viaţă, deşi luând în
        considerare natura militară a Ordinului, aceasta putea fi o perioadă foarte scurtă. Numai doi Mari Maeştri au murit în timpul mandatului, iar câţiva au murit în timpul
        campaniilor militare. De exemplu, în timpul Asediului Ascalonului din 1153, Marele Maestru Bernard de Tremelay a condus un grup de 40 de templieri printr-o spărtură în
        zidurile oraşului. Atunci când restul armatei cruciate nu i-a urmat, templierii, inclusiv Marele lor Maestru, au fost încercuiţi şi decapitaţi. Marele Maestru Gérard
        de Ridefort a fost decapitat de către Saladin în 1189, în timpul Asediului Acrei.

    <p>Marele Maestru supraveghea toate operaţiunile Ordinului, atât cele militare din Ţara Sfântă şi Europa de Est, cât şi afacerile financiare din Europa Occidentală. Unii
        Mari Maeştri au fost şi comandanţi militari, deşi acest lucru nu era întotdeauna înţelept: câteva defecte în strategia de luptă a lui de Ridefort au contribuit la
        devastatoarea înfrângere de la Bătălia de la Hattin. Ultimul Mare Maestru a fost Jacques de Molay, ars pe rug la Paris în 1314, din ordinul regelui Filip al IV-lea al
        Franţei.

    </p>
