    <h2 onclick="slide(div1,s1)" style="position: relative; text-align: left">Organizarea<span id="s1"> ᐱ</span></h2>
    <div class="block" id="div1">
    <p>Templierii erau organizaţi drept un ordin monahal, similar cu Ordinul cistercian al lui Bernard de Clairvaux, care era considerat drept prima organizaţie internaţională
        eficace din Europa. Structura organizaţională avea un puternic lanţ al autorităţii. Fiecare ţară cu o prezenţă notabilă a templierilor (Franţa, Anglia, Aragon,
        Portugalia, Poitou, Apulia, Regatul Ierusalimului, Comitatul de Tripoli, Principatul de Antiohia, Anjou şi Ungaria) avea un Maestru al Ordinului Templierilor în
        acea regiune. Toţi aceştia răspundeau în faţa Marelui Maestru (întotdeauna un cavaler francez), numit pe viaţă, care supraveghea atât eforturile militare ale Ordinului
        din est, cât şi proprietăţile financiare din vest. Nu se cunoaşte un număr precis, însă se estimează că, în perioada de glorie a Ordinului, existau între 15000 şi 20000
        de templieri, dintre care doar o zecime erau cavaleri adevăraţi.

    <p><img class="image left" src="../image/flag.jpeg" alt="Steag a Cavalerilor Templieri">
        Bernard de Clairvaux şi fondatorul Hugues de Payens au fost cei care au redactat codul specific de comportament al Ordinului Templierilor, cunoscut istoricilor moderni
        sub numele de Norma Latină. Cele 72 de clauze ale sale defineau comportamentul ideal al cavalerilor, cum ar fi tipurile de robe pe care trebuiau să le poarte sau câţi
        cai puteau avea. Cavalerii trebuiau să ia masa în linişte, să nu mănânce carne mai mult de trei ori pe săptămână şi nu aveau voie să aibă contact fizic cu nicio femeie,
        nici măcar cu membrii propriei familii. Unui Maestru al Ordinului îi erau atribuiţi "4 cai, unui frate capelan sau unui preot 3 cai, unui frate sergent 2 cai şi unui
        gentleman valet, pentru a-şi căra scutul şi lancea, un cal". Pe măsură ce ordinul creştea, au fost adăugate din ce în ce mai multe îndrumări, lista originală de 72
        de clauze ajungând în final până la câteva sute.

    <p>Rândurile templierilor erau împărţite în trei categorii: cavalerii aristocraţi, sergenţii proveniţi din oameni de rând şi clericii. Cavalerii trebuiau să fie de origine
        aristocratică şi să poarte mantii albe. Erau echipaţi drept cavalerie grea, cu trei sau patru cai şi cu unul sau doi paji. Pajii, în general, nu erau membri ai Ordinului,
        ci erau străini angajaţi pentru o anumită perioadă de timp. Din categoria poziţionată sub cavaleri ca importanţă făceau parte sergenţii, aleşi din rândurile populaţiei
        obişnuite. Erau ori echipaţi drept cavalerie uşoară cu un singur cal, ori serveau în alte moduri, cum ar fi administrarea proprietăţii Ordinului, treburi
        domestice sau comerţ. Capelanii, constituind a treia clasă a templierilor, erau preoţi consacraţi ce aveau grijă de nevoile spirituale ale templierilor.

    <p>Cavalerii purtau robe albe cu o cruce roşie şi o manta albă; sergenţii purtau o tunică neagră cu o cruce roşie în faţă şi în spate şi o manta neagră sau maro. Mantaua albă
        a fost atribuită templierilor la Conciliul de la Troyes în 1129, iar crucea a fost adăugată robelor cel mai probabil la începutul celei de-a doua cruciade, când Papa Eugen
        al III-lea, regele Ludovic al VII-lea al Franţei şi multe alte personalităţi au luat parte la o întâlnire a templierilor francezi, la sediul lor de lângă Paris. Potrivit
        Normei lor, cavalerii trebuiau să poarte mantaua albă întotdeauna, fiindu-le interzis chiar şi să bea sau să mănânce dacă nu o purtau.

    <p>Iniţierea, cunoscută sub numele de Recepţie (receptio), în cadrul Ordinului era un profund angajament şi implica o ceremonie solemnă. Necunoscuţii erau descurajaţi a
        participa la ceremonie, ceea ce a trezit suspiciuni în rândurile inchizitorilor medievali în timpul proceselor ulterioare.

    <p>Noii membri trebuiau să semneze de bună voie predarea întregii averi şi proprietăţi Ordinului şi să facă jurăminte de sărăcie, castitate, pietate şi obedienţă. Majoritatea
        fraţilor se alăturau pe viaţă, deşi unora le era permis să se alăture şi pe o anumită perioadă de timp. Uneori, unui om însurat îi era permis să se alăture dacă avea
        permisiunea soţiei, însă nu avea voie să poarte mantaua albă.

    <p>Crucea roşie pe care templierii o purtau pe robe era un simbol al martiriului, căci a muri în luptă era considerat o mare onoare, care asigura un loc în rai. Era o lege de
        căpătâi care sublinia faptul că războinicii Ordinului nu ar trebui să se predea niciodată, decât dacă steagul templierilor ar fi căzut, însă şi atunci să fie primii care
        încercau să se regrupeze cu un alt ordin creştin, cum ar fi cel al Ospitalierilor. Numai după ce toate steagurile ar fi căzut le era permis să părăsească câmpul de bătălie.
         Acest principiu necompromiţător, precum şi reputaţia lor pentru curaj, pregătirea lor excelentă şi armamentul greu, au făcut din templieri una dintre forţele de luptă cele
        mai de temut din timpurile medievale.
