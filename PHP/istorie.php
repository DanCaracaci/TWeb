    <h2 onclick="slide(div1,s1)" style="position: relative; text-align: left">Începutul<span id="s1">ᐱ</span> </h2>
    <div class="block" id="div1">
    <p><img class="image left" src="../image/temple_Mount.jpg" alt="Moscheea Al Aqsa">
        Templierii apar imediat după prima Cruciadă. În 1119, un nobil francez din regiunea Champagne, Hugues de Payens, împreună 8 cavaleri aleşi dintre rudele sale, a creat
        Ordinul Cavalerilor Templieri. Misiunea lor declarată era de a proteja pelerinii din Ţara Sfântă. Cu acordul regelui Baldvin al II-lea al Ierusalimului, şi-au înfiinţat
        sediul pe Muntele Templului, unde este acum Moscheea Al Aqsa.
        <img class="image right" src="../image/temple-dome.jpg" alt="Domul Pietrei">

    <p>Muntele Templului este sacru pentru Evrei, Creştini şi pentru Musulmani, deoarece este o locaţie importantă în istorie. Se crede că aici ar fi ruinele din Templul
        lui Solomon, unde s-ar fi ascuns iniţial Sfântul Graal. Este o locaţie importantă şi pentru musulmani. Se spune că aici este piatra de pe care s-a ridicat la cer
        Mohamed, iar în secolul al VII-lea, Califul Abd al Malik a construit o importantă moschee, Domul din Piatră. Cruciaţii au transformat-o în catedrală, numind-o "Templum
        Domini", şi astfel şi-au luat numele de Templieri. Catedrala a devenit un model pentru catedralele Templierilor, precum Catedrala templului din Londra. Nu se ştiu foarte
        multe despre activitatea ordinului din primi 9 ani. Dar în 1128 ei au devenit foarte cunoscuţi în Europa. Au început o campanie de strângere de fonduri, prin care
        solicitau bani, pământuri sau chiar pe fiii nobililor, pentru a se alătura ordinului. Eforturile lor au fost susţinute de un personaj proeminent din partea bisericii
        Bernard din Clairvaux (mai târziu a fost sanctificat), el fiind nepotul unuia din cei 9 templieri care au format iniţial Ordinul. În acelaşi an a avut loc Conciliul de
        la Troyes, în urma căruia Ordinul a primit recunoaşterea oficială. În 1130 Regele Aragonului din Spania a lăsat prin testament mari întinderi de pământ. Donaţiile au
        devenit o practica folosită de toţi noii membri.

    <p><img class="image right" src="../image/bernhard.jpg" alt="Sf. Bernard de Clairvaux, patronul Ordinului, dar şi al cisternienilor">
        In 1139, Papa Inocenţiu al II-lea a dat un ordin prin care Cavalerii Templieri deveneau şi mai puternici. Acest ordin permitea Cavalerilor Templieri să treacă liber
        graniţele, să fie excluşi de la plata taxelor, iar singura autoritate recunoscută rămânea doar Papa. Aceasta era o confirmare a puterii lor, care se pare ca a fost
        susţinută de patronul Ordinului, Bernard de Clairvaux, pentru că l-a ajutat pe Papa Inocenţiu să conducă biserica Catolică.
    </div>

    <h2 onclick="slide(div2,s2)" style="position: relative; text-align: left">Ascensiunea<span id="s2">ᐱ</span></h2>
    <div class="block" id="div2">
    <p>După ce Ierusalimul a fost cucerit în iulie 1099 ca urmare a primei cruciade, mulţi pelerini europeni s-au îndreptat spre locurile numite de ei Ţara Sfântă. Totuşi,
        deşi oraşul era relativ sub control, restul statelor cruciate nu erau. Bandiţii erau prezenţi în număr mare, şi mulţi pelerini erau măcelăriţi, uneori cu sutele, pe
        drumul de la Jaffa, situat pe coastă, în Ţara Sfântă.

    <p>În jurul anului 1119, cavalerul francez Hugues de Payens şi ruda sa Godfrey de Saint-Omer, veterani ai primei cruciade, propun crearea unui ordin monastic (călugăresc)
        pentru protecţia acestor pelerini. Regele Balduin al II-lea al Ierusalimului a acceptat această cerere, oferindu-le drept cartier general Muntele Templului, în
        moscheea Al Aqsa care fusese capturată. Muntele templului avea un aer mistic, deoarece se credea că se află deasupra ruinelor Templului lui Solomon. Cruciaţii
        numeau prin urmare Moscheea Al Aqsa Templul lui Solomon, şi de aici ordinul şi-a tras numele de Sărmanii soldaţi ai lui Cristos şi ai Templului lui Solomon, sau
        cavalerii "templieri". Cu puţine resurse financiare la dispoziţie, ordinul de aproximativ nouă cavaleri se baza pe donaţii pentru a supravieţui. Emblema lor era
        reprezentată de doi cavaleri ce călăreau acelaşi cal, simbol al sărăciei.
        <img class="image right" src="../image/Aqsa_Mosque.JPG" alt="Primul cartier general al templierilor, Moscheea Al Aqsa,">

    <p>
        Sărăcia templierilor nu a durat multă vreme. Ordinul avea un sprijin important în persoana lui Bernard de Clairvaux, un cleric de frunte, şi nepot al unuia dintre
        cavalerii fondatori. El a vorbit şi scris cu convingere în numele lor, şi în 1129, la Conciliul din Troyes, Ordinul a fost oficial recunoscut de Biserică. O dată cu
        acest act formal, templierii au devenit o destinaţie preferată a donaţiilor din Europa, primind bani, pământuri şi fii de nobili de la familii dornice să sprijine
        lupta (considerată creştină) din Ţara Sfântă. Un alt beneficiu l-au primit în 1139, când bula papală a papei Inocenţiu al III-lea Omne Datum Optimum i-a scutit pe
        templieri de la supunerea la legile locale. Acest lucru însemna că templierii puteau traversa liber orice graniţe (în Europa de vest), nu trebuiau să plătească taxe,
        şi nu răspundeau decât în faţa papei.

    <p>Cu o misiune clară şi cu resurse abundente la dispoziţie, Ordinul a crescut repede. Templierii erau adesea forţa de avangardă în bătălii cheie ale Cruciaţilor, când
        cavalerii în armură atacau frontal, într-un dispozitiv în formă de vârf de lance, în încercarea de a sparge liniile adversarilor. Una dintre cele mai mari victorii a
        fost la Bătălia de la Montgisard, din 1177, când o armată creştină din care făceau parte şi circa 500 de templieri a reuşit să învingă o armată a lui Saladin de peste
        26.000 de soldaţi.

    <p>Deşi misiunea principală a Ordinului era militară, doar un număr mic de membri se afla efectiv pe frontul de luptă. Restul aveau un rol de sprijin (un fel de spate al
        frontului, "templier") , atât pentru ajutorul individual al cavalerilor, cât şi pentru a asigura buna funcţionare a infrastructurii financiare. Templierii, deşi făcuseră
        un jurământ de sărăcie, în realitate în timp au ajuns să controleze bogăţii ce depăşeau cu mult donaţiile directe pentru cauza lor. Unii nobili participanţi la cruciade
        îşi depuneau averea în mâinile templierilor, pentru perioada cât erau plecaţi. Acumularea de bogăţii în acest mod în Europa şi Ţara Sfântă a dus la apariţia primelor
        scrisori de credit, pentru pelerinii ce călătoreau în Ţara Sfântă. La plecare, ei îşi depuneau averea unui cavaler din regiunea respectivă, de la care primeau un document
        încifrat, indicând valoarea sumei deţinute, pe care îl foloseau la sosirea în Ţara Sfântă pentru recuperarea fondurilor; acest sistem a îmbunătăţit atât siguranţa
        pelerinilor, care nu mai erau vizaţi de hoţi, dar a şi sporit nemeritat averea templierilor.

    <p>Pe baza acestui amestec de donaţii şi de afaceri, templierii au stabilit o reţea financiară în întreaga creştinătate. Deţineau suprafeţe întinse atât în Europa cât şi în
        Orientul Mijlociu; cumpărau şi foloseau ferme şi podgorii; construiau biserici şi castele; erau implicaţi în manufactură, importuri şi exporturi; aveau propria lor flotă
        navală, şi, pentru un timp, deţineau întreaga insulă Cipru. Templierii au fost numiţi uneori ca prima corporaţie multinaţională, care deţinea aproape 1000 de
        comanderii (unităţi administrative locale cu reşedinţe pentru membri) şi fortificaţii în Europa.
    </div>

    <h2 onclick="slide(div3,s3)" style="position: relative; text-align: left">Declinul<span id="s3">ᐱ</span></h2>
    <div class="block" id="div3">
    <p><img class="image right" src="../image/battle.jpg" alt="Bătălia de la Hattin, din 1187">
        La jumătatea secolului al XII-lea, cruciadele începuseră să-şi piardă puterea, deoarece nu întotdeauna îşi atingeau scopul reclamat. Lumea musulmană era mai unită sub
        lideri eficienţi precum Saladin, iar facţiunile creştine erau măcinate de lupte interne. Templierii s-au aflat uneori în conflict cu celelalte două mari ordine
        creştine, cavalerii ospitalieri şi cavalerii teutoni (germanici), iar deceniile de lupte pentru domeniile feudale au slăbit poziţiile creştine. După mai multe
        înfrângeri dezastruoase, incluzând bătălia decisivă de la Hattin, Ierusalimul a fost pierdut în faţa lui Saladin în 1187. Cruciaţii au ocupat din nou oraşul în abia în
        1229 (fără ajutor de la templieri), dar numai pentru scurt timp. În 1244, musulmanii au reocupat oraşul, care nu a mai fost controlat vreodată de creştini până în 1917,
        când britanicii l-au luat de la otomani.

    <p>Templierii au fost obligaţi să îşi mute cartierul general în alte oraşe din nord, cum ar fi portul Acra, pe care l-au deţinut secolul următor. A fost însă până la urmă
        pierdut în 1291, urmat de ultimele lor fortăreţe de pe continent, Tortosa (în Siria de astăzi) şi Atlit. S-au stabilit în continuare în Limassol, Cipru, păstrând o
        garnizoană pe mica insulă Arwad, lângă coastă, în apropiere de Tortosa. S-au încercat acţiuni militare coordonate cu mongolii, printr-o nouă forţă de invazie de pe
        Arwad. În 1302, totuşi, ei au pierdut şi acea insulă, ultimul loc din Ţara Sfântă aflat în mâinile lor.

    <p><img class="image left" src="../image/Champs_Chapelle.jfif" alt="Clădire templieră la Saint Martin des Champs, Franţa">
        Misiunea militară a ordinului nemaifiind la fel de importantă, sprijinul acordat organizaţiei s-a risipit. Situaţia era totuşi mai complicată, căci după mai mult de
        două sute de ani de existenţă, templierii deveniseră o parte a vieţii social-economice zilnice în Europa. Organizaţia era prezentă pe scară largă la nivel local, cu
        mii de sedii în toată Europa. Templierii administrau multe întreprinderi, şi mulţi europeni intrau zilnic în contact cu ei, de exemplu lucrând la o fermă sau podgorie,
        ori folosind Ordinul ca pe o bancă în care să îşi depoziteze averea personală. Ordinul, însă, rămânea în continuare independent de stăpânirea locală (deci era "neimpozabil"),
        practic fiind un "stat în stat". Avea de asemenea o armată cu care putea străbate liber toate graniţele, însă nu mai avea un câmp de luptă clar, adică un adversar acceptabil
        şi pentru restul societăţii. Această situaţie a mărit tensiunile cu o parte a nobilimii europene, în contextul în care templierii se arătau interesaţi în fondarea unui propriu
        stat monastic (călugăresc), după cum făcuseră cavalerii teutoni (germanici) în Prusia după 1226.
    </div>

    <h2 onclick="slide(div4,s4)" style="position: relative; text-align: left">Arestările și dizolvarea<span id="s4">ᐱ</span></h2>
    <div class="block" id="div4">
    <p>În 1305, noul papă, Clement al V-lea, aflat în Franţa, a trimis scrisori Marelui Maestru templier Jacques de Molay şi celui ospitalier Fulk de Villaret pentru a discuta
        posibilitatea fuziunii celor două ordine. Niciunul nu agrea ideea, dar papa a insistat şi i-a invitat pe amândoi în Franţa pentru a discuta situaţia. De Molay a sosit
        primul la începutul lui 1307, dar de Villaret a întârziat câteva luni. În timp ce aşteptau, De Molay şi Clement au discutat acuzaţiile aduse cu doi ani înainte de un
        templier exclus (din ordin). Aceste acuzaţii erau în general considerate false, dar Clement i-a trimis regelui Filip al IV-lea al Franţei o cerere scrisă pentru ajutor
        în investigaţie. Regele era deja îndatorat templierilor după războiul său cu englezii, şi a decis să folosească zvonurile existente în propriul său scop. A început să
        pună presiune pe Biserică să acţioneze împotriva ordinului, pentru a se elibera de datorii.

    <p>În data de vineri, 13 octombrie, 1307 (o dată legată greşit de originea superstiţiei zilei de vineri 13), Filip a ordonat ca Molay şi alţi templieri francezi să
        fie arestaţi simultan. Ei au fost acuzaţi de numeroase erezii, şi torturaţi pentru obţinerea de mărturisiri de blasfemie. Mărturisirile, deşi obţinute sub presiune, au
        provocat un scandal la Paris. După alte insistenţe din partea lui Filip, papa Clement a emis bula Pastoralis Praeeminentiae la 22 noiembrie, 1307, care cerea tuturor
        monarhilor creştini din Europa să-i aresteze pe templieri şi să le confişte averile.

    <p>Papa Clement a cerut audieri papale pentru a determina vinovăţia sau nevinovăţia templierilor, şi, o dată eliberaţi de tortura inchizitorilor, mulţi şi-au retras
        mărturisirile. Unii aveau suficientă experienţă legală pentru a se apăra în procese, dar în 1310, Filip le-a blocat tactica, folosind mărturiile forţate precedente ca
        justificare pentru arderea pe rug a zeci de templieri la Paris.

    <p><img class="image left" src="../image/templars_on_stake.jpg" alt="Templieri arşi pe rug">
        <img class="image right" src="../image/Tomartemplarschurch.jpg" alt="Mănăstirea lui Cristos din castelul Tomar, Portugalia">
        Cum Filip ameninţa cu acţiuni militare dacă papa nu îi respecta dorinţele, Clement a acceptat în cele din urmă dizolvarea ordinului, invocând scandalul public provocat
        de mărturisiri. La Conciliul de la Vienne din 1312, a emis o serie de bule papale, între care Vox in excelso, care dizolva oficial Ordinul, şi Ad providam, care oferea
        cea mai mare parte a posesiunilor templierilor ospitalierilor.

    <p>Cât despre conducătorii Ordinului, Marele Maestru Jacques de Molay, care mărturisise sub tortură, şi-a retras declaraţia. Asociatul său, Geoffrey de Charney, preceptor al
        Normandiei, i-a urmat exemplul, şi a insistat pe nevinovăţia sa. Ambii au fost declaraţi vinovaţi de a fi redevenit eretici, şi au fost condamnaţi să ardă pe rug la
        Paris, pe 18 martie, 1314. Se spune că de Molay a rămas sfidător până la sfârşit, cerând să fie legat astfel încât să fie cu faţa la catedrala Notre Dame, şi să îşi ţină
        mâinile împreunate. După legendă, a strigat dintre flăcări că atât papa Clement, cât şi regele Filip îl vor întâlni în curând în faţa lui Dumnezeu. Papa Clement a
        murit o lună mai târziu, iar regele Filip într-un accident de vânătoare înainte de sfârşitul anului.

    <p>Odată cu dispariţia conducătorilor Ordinului, restul templierilor din Europa au fost fie arestaţi şi judecaţi (nefiind aproape în totalitate condamnaţi), absorbiţi în
        alte ordine militare, precum Cavalerii ospitalieri sau Ordinul de Calatrava, sau lăsaţi să se retragă şi să trăiască în continuare în pace. E posibil ca unii să fi fugit
        în teritorii ce nu erau sub controlul papei, precum Scoţia excomunicată. Acolo, au luptat pentru regele Robert Bruce (la acea vreme excomunicat şi el). Templierii au avut
        o influenţă importantă asupra istoriei Scoţiei, inclusiv în încercările de restaurare a dinastiei Stuart în Regatul Unit. Organizaţia templieră din Portugalia doar şi-a
        schimbat numele, din Ordinul Templului în Ordinul lui Cristos.

    <p>În 2001, un document cunoscut ca "Pergamentul de la Chinon" a fost descoperit în Arhivele Vaticanului, după ce aparent a fost catalogat greşit în 1628. Este o
        înregistrare a procesului templierilor şi arată că papa i-a absolvit iniţial pe templieri de toate ereziile în 1308, înainte de a desfiinţa ordinul în 1312. În
        octombrie 2007, documentele secrete despre procesul templierilor, între care şi pergamentul de la Chinon, au fost publicate de Vatican.

    <p>În prezent, poziţia Bisericii Romano-Catolice este că persecuţia medievală a templierilor a fost nedreaptă; că nu era nimic rău legat de Ordin sau de membrii săi; şi că
        papa Clement a fost obligat să acţioneze de magnitudinea scandalului public şi de influenţa dominatoare a regelui Filip al IV-lea.
    </p>
